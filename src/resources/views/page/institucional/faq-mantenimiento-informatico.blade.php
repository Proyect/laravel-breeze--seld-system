
<h2 class="bg-primary text-center">Mantenimiento Informatico</h2>
		<h3>¿En qué consiste un mantenimiento informático?</h3>
		<p>Son las acciones llevadas a cabo para cubrir las necesidades o incidencias técnicas que surjan en su empresa y a la vez mantener en perfecto estado 
			el funcionamiento de todo lo que refiere al sistema informático, tanto hardware, cómo software.</p>
			
		<h3>¿Por qué contratar un mantenimiento informático?</h3>
		<p>En Infrasoft Servicios Informáticos, nos enfocamos en la pequeña y medianas empresa, puesto que la gran mayoría de las grandes empresas tienen los medios 
			necesarios disponiendo de un departamento de informática.
		</p>
		<p>Lo que ignoran estas empresas en la mayoría de las ocasiones es el servicio profesional, especializado y el abaratamiento de los precios, que ofrecemos
			 cuotas fijas con una amplia gama de servicios con los que su empresa estará siempre asegurada.</p>
			 
		<h3>¿En qué se diferencia este de otros servicios?</h3>
		<p>Quienes se dedican a este servicio deben de estar constantemente actualizando sus conocimientos y dando un servicio integral, ya que la informática es un
			 mundo muy amplio y cambia cada día.
		</p>
		<p>El servicio más importante a tener en cuenta, es la prevención ante cualquier tipo de incidencia y contingencia, en este sentido ahorraremos en costos a 
			mediano y largo plazo.
		</p>
		
		<h3>¿Qué es un servicio de mantenimiento informático?</h3>
		<p>Las empresas de hoy necesitan utilizar la informática para la gestión y desarrollo de su negocio.  El mantenimiento informático consiste en la actualización, 
			reparación y optimización de todo elemento de software o hardware.</p>
		
		<h3>¿Cuales son las áreas principales del mantenimiento informático?</h3>
		<p>Podemos hablar de al menos estas tres ramas del mantenimiento:</p>
		<ul>
			<li>	Mantenimiento de ordenadores: todo lo relacionado con problemas de hardware y software, de redes informáticas y problemas de seguridad para los 
				ordenadores de tu empresa, sean portátiles u ordenadores de mesa.</li>			
			<li>	Matenimiento de servidores: administración, gestión y monitorización de servidores de la empresa, lo cual suele representar una parte muy sensible del 
				negocio y que si se deja sin mantenimiento puede resultar fatal.</li>
			<li>	Mantenimiento de software: es lo que entendemos como el mantenimiento de aplicaciones para empresas, por ejemplo si has desarrollado o quieres 
				desarrollar un software especializado para utilizarlo en tu negocio, requerirá de mantenimiento para que siga funcionando, revisar y comprobar posibles 
				vulnerabilidades, etc.</li>
		</ul>

		
		<h3>¿Cómo es un mantenimiento preventivo?</h3>
		
		<p>Un mantenimiento preventivo consiste  en la conservación de instalaciones mediante realización de procedimientos, revisiones y reparaciones, así como implantación 
			y análisis de sistemas de seguridad y automatización de copias de seguridad que garanticen su buen funcionamiento y fiabilidad de todo sistema informático, 
			para evitar el mayor número de incidencias informáticas.</p>
			
		