        <h3> Etapas del Desarrollo de un Sitio Web</h3>
                
		<p>Para planificar y llevar a cabo un proyecto necesario para su empresa, este mismo necesita los siguientes pasos:</p>
                
                <h4>1) Contacto: </h4>
                
                <p>El cliente nos informa de sus necesidades y requerimiento, los fines y 
                objetivos del proyecto a realizar. Es aquí donde se realiza el relevamiento de
                datos.</p>
                
                <h4> 2) Planificación: </h4>
                
                <p> Según los datos adquiridos en el relevamiento de datos, considerando la complejidad de encargar el
                	 proyecto, los costos del mismo, plazos a cumplir. Se realizan varios análisis para ver la factibilidad, 
                	 la tecnología a implementar y determinar los costos del mismo
                </p>
                
                <h4> 3) Diseño: </h4>
                
                <p>Se definen detalles que surgieron en la planificación, se realiza un refinamiento de la planificación y
                	 se genera un modelo del proyecto que servirá de base en el desarrollo, con la previa aprobación por 
                	 parte del cliente.
                </p>
                
                <h4> 4) Desarrollo: </h4>
                
                <p>Es la puesta en marcha de los pasos anteriormente mencionados, es el punto donde la teoría pasa a ser 
                	la práctica en si, se genera el código, bases de datos, interfaz, etc.
                </p>
                
                <h4> 5) Prueba:</h4>
                
                <p>En esta etapa se hace una búsqueda exhaustiva de posibles errores o problemas que puedan haber surgido 
                	en la etapa de desarrollo
                </p>
                
                <h4> 6) Puesta en marcha: </h4>
                
                <p>Aquí se finalizo la etapa de prueba, concluyendo que el sitio ya es estable y por lo tanto es puesto 
                	online y difundido en Internet.
                </p>
                
