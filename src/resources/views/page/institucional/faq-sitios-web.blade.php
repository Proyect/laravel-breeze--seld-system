		<h3>¿Qué es un sitio Web?</h3>
        
        <p>Un sitio Web es básicamente un conjunto organizado y coherente de diversas páginas Web (generalmente archivos
        	 en formato html, php, cgi, etc.) Y otros archivos, ya sean gráficos, animaciones, sonidos, etc. 
        </p>
		<p>	 
        	 A través de un sitio Web podemos ofrecer, informar, publicitar o vender, productos o 
                servicios.
        </p>
                
