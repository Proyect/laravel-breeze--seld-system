                <h3>¿Cuanto tiempo requerirá desarrollar mis Sitio Web?</h3>
                
                <p>Es muy difícil determinar con precisión el tiempo necesario para el 
                desarrollo de un sitio Web. Influyen una serie de causas como pueden ser:</p>
                <ul class='list' >
					<li> Lo claras que tenga sus ideas de la Web.</li>
					<li> El alcance y complejidad del sitio Web.</li>
					<li> Los contenidos que se van a incluir.</li>
					<li> La traducción a otros idiomas si se requiere</li>
				</ul>             
                
