
<h2 class="bg-primary text-center">Desarrollo de sistemas</h2>
		<h3>¿Que es un software?</h3>
		
		<p>Conjunto de programas y rutinas que permiten a la computadora realizar determinadas tareas. 
		</p>
		<p>	
			El software es desarrollado mediante distintos lenguajes de programación, que permiten controlar el 
			comportamiento de una máquina. Estos lenguajes consisten en un conjunto de símbolos y reglas sintácticas
			 y semánticas, que definen el significado de sus elementos y expresiones.
		</p>
		<p>		 
			 Un lenguaje de programación permite a los programadores del software especificar, en forma precisa, sobre
			  qué datos debe operar una computadora.
		</p>
		
		<h3>¿Que son los lenguajes de programacion?</h3>
		
		<p>Un lenguaje de programación es un lenguaje formal que proporciona una serie de instrucciones que permiten a
			 un programador escribir secuencias de órdenes y algoritmos a modo de controlar el comportamiento físico y 
			 lógico de una computadora con el objetivo de que produzca diversas clases de datos.
		</p>
		<p>		 
			  A todo este conjunto de órdenes y datos escritos mediante un lenguaje de programación se le conoce como programa. 
		</p>
			
		<h3>¿Que es un sistema informatico?</h3>
		
		<p>
			Básicamente, un sistema informático, más conocido en el ámbito de la tecnología por sus siglas “SI” es una técnica 
			que permite el almacenamiento y el proceso de información, para lo cual se vale de un grupo de elementos que se 
			relacionan entre sí.
		</p>
		
		<h3>¿Cuáles son las funciones de un sistema informático?</h3>
		
		<p>Un sistema informático (SI) es un sistema que permite almacenar y procesar información; es el conjunto de partes
			 interrelacionadas: hardware, software y personal informático.
		</p>	 
	