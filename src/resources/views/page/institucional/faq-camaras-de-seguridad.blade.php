	<h2 class="bg-primary text-center">Camaras de Seguridad</h2>
	<h3>¿Que son las camaras de seguridad?</h3>
	<p>Una cámara IP es una cámara que emite las imágenes directamente a la intranet o internet sin necesidad de un ordenador.</p>
	
	<h3>¿Cuál es el precio de la instalación de cámaras de seguridad profesionales?</h3>
	<p>Primeramente se realiza un presupuesto acorde a sus requerimientos y necesidades. En tal presupuesto se expresan la 
		metodología de trabajo, la tecnologías a implementar, el costo de materiales y mano de obra. Y por supuesto el tiempo de 
		desarrollo, con las garantías correspondientes</p>
	
	<h3>¿Tiene costo adicional el ver mis cámaras por internet o desde mi teléfono?</h3>
	<p>NO, para ver sus cámaras por internet o celular usted solo necesita tener su equipo de recepción y emisión conectado a 
		internet.</p>	
	<p>Usted podrá visualizar las cámaras de seguridad, desde cualquier punto del planeta, a su vez también podrá ver sus cámaras 
		desde una computadora (en su oficina por ejemplo) o con un teléfono o tablet con conexión a internet. </p>
	<p>Nosotros nos encargamos de capacitarlo para poder visualizar los equipos. 	</p>
	
	<h3>¿Puedo grabar lo que captan las cámaras de vigilancia?</h3>
	<p>SI. Los equipos tienen la opción de incluir uno o varios  discos duros,  donde se puede guardar la grabación dependiendo de la cantidad de cámaras, la calidad de la
		 imagen y la capacidad de almacenaje.</p>
	
	<h3>¿Qué tipo de cámaras existen y cual necesito?</h3>
	<p>Los tipos de cámaras generalmente se diferencian por su forma o función como ejemplo de forma están los tipo domo, bullet (en forma de bala) o cubo y las que tienen una 
		función especial serian las PTZ.</p>
	<p>Todas estas cámaras podrían tener características o funciones de acuerdo a la necesidad como:</p>
	
	<ul class="list">
		<li>Visión nocturna (leds o función día/noche)</li>
		<li>Tipo de lente (2.8, 3.6, varifocal)</li>
		<li>Resolución análoga (TVL) o digital (HD 720p FHD 1080p, 4K, etc)</li>
		<li>Para exterior o interior.</li>
		<li>Zoom optico (acercamiento) manual o electrónico</li>
		<li>Alambrica o Inalambrica (Wi-fi)</li>
		<li>Color, audio, antivandalica, etc.</li>
	</ul>
	<p>Para saber qué tipo de cámara necesita debe tomar en cuenta los siguientes aspectos:</p>
	<ul class="list">
		<li>Si estan en interior o exterior.</li>
		<li>Si van trabajar sin luz y si es así saber que distancia que se requiere ver en la obscuridad (a más distancia, más leds).</li>
		<li>Tamaño del area que van a captar, entre mas grande sea el área mayor distancia focal (por ejemplo 6mm) y si necesitan enfocar un aarea chica como un acceso peatonal 
la distancia focal debería ser menor (por ejemplo 2.8).</li>
		<li>Si requiere audio en una zona.</li>
		<li>Si requiere mas definición de imágenes (entre mas definición, mas caro es el equipo).</li>
		<li>Si requiere cámaras con movimiento (PTZ).</li>
		
	</ul>
	<p>Como verá, son muchas las variables y en la mayoría de los casos nuestros paquetes cubren las necesidades típicas pero si usted tiene dudas y necesita asesoría, nos 
		encantaría poder platicarle las opciones que tenemos especialmente para usted.</p>
	
	<h3>¿Qué resoluciones (calidad de video) tienen las cámaras o los equipos?</h3>
	<p>Hay cuatro tecnologías principales; Análoga, IP, HDCVI y AHD y cada una de estas tiene una variedad de resoluciones distinta.</p>
	
	<p>La Análoga esta en desuso y la resolución se puede medir por TVL (lineas de resolución) que van de 300 a 1200 TVL.</p>
	
	<p>La IP puede dividir su resolución en megapixeles que van de 1.3 a 6 megapixeles o en HD que van de 720p a 1080p. Estas cámaras son múy 
		versátiles ya que no requieren DVR (grabador digital) y por lo mismo solo se pueden ver por internet o por medio de equipos que concentren 
		su salida de video. Tome en cuenta que aunque la resolución de estas cámaras es muy buena, su costo es elevado y aumenta a medida de la 
		calidad de resolución.</p>
	
	<p>HDCVI es una tecnología de Dahua y nativamente tiene resoluciones que van de HD 720p, FHD 1080p A 4 megapixeles.</p>
	
	<p>AHD es similar en resolucion a HDCVI pero no soporta tanta distancia de transmisión como HDCVI.</p>
	
