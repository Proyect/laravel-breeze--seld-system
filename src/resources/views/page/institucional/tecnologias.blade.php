	<p>Desde un punto de vista arquitectónico y técnico CodeIgniter fue creado con los siguientes objetivos:
	<ul>
    <li>Instanciación dinámica. En CodeIgniter, los componentes y rutinas son cargados únicamente cuando son invocados, haciendo
     posible que la aplicación sea muy liviana. </li>
    <li>acoplamiento. El acoplamiento es el grado que los componentes de un sistema dependan unos de otros. Cuanto menos acomplado 
    esten los componentes, más reusable y flexible será el sistema.</li> 
    <li>Singularidad del componente. La singularidad es el grado que más componentes tienen un propósito en el que enfocarse más
     estrecho. En nuestro caso, cada clase y sus funciones son altamente autónomas para permitir máxima utilidad.</li> 
	</ul>
